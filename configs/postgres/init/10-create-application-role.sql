CREATE ROLE crawler_app
  LOGIN
  NOSUPERUSER
  NOCREATEDB
  NOCREATEROLE
  NOINHERIT
  NOREPLICATION
  PASSWORD 'local_app_only';

REVOKE ALL ON DATABASE crawler FROM PUBLIC;
GRANT CONNECT ON DATABASE crawler TO crawler_app;

\connect crawler

REVOKE CREATE ON SCHEMA public FROM PUBLIC;
GRANT USAGE ON SCHEMA public TO crawler_app;
ALTER DEFAULT PRIVILEGES FOR ROLE crawler_owner IN SCHEMA public
  GRANT SELECT, INSERT, UPDATE, DELETE ON TABLES TO crawler_app;
ALTER DEFAULT PRIVILEGES FOR ROLE crawler_owner IN SCHEMA public
  GRANT USAGE, SELECT ON SEQUENCES TO crawler_app;
